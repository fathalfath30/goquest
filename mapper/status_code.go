package mapper

import (
	"errors"
	"net/http"
)

func StatusCode(response *http.Response) error {
	if response.StatusCode >= 100 && response.StatusCode <= 299 {
		return nil
	}

	return errors.New(http.StatusText(response.StatusCode))
}
